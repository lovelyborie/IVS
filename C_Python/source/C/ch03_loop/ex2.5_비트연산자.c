#include <stdio.h>
// 비트연산자 : &(and), |(or) , ^(XOR : 같으면 0 다르면 1)

int main(void) {
    int n1 = 10 ; // 000...1010 (2진수저장)
    int n2 = 6;   // 000...0110
    // n1 & n2    :  000...0010 (2)
    // n1 | n2    :  000...1110 (14)
    // n1 ^ n2    :  000...1100 (12)
    printf("n1&n2 = %d\n",n1&n2);
    printf("n1|n2 = %d\n",n1|n2);
    printf("n1^n2 = %d\n",n1^n2);
    printf("n2 << 1 = %d\n",n2<<1); // 000...00110  => 000...01100 (<<1 은 2배)
    printf("n2 >> 1 = %d\n",n2>>1); //(>>1 은 1/2배)
}